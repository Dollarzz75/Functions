// 11. Assign a value to a variable using the += operator
let a = 10;
a += 5; // same as a = a + 5
console.log("Value of a:", a); // 15


// 12. Use the -= operator to decrease a variable by 10
let b = 25;
b -= 10; // same as b = b - 10
console.log("Value of b:", b); // 15


// 13. Multiply a variable by 4 using *= operator
let c = 7;
c *= 4; // same as c = c * 4
console.log("Value of c:", c); // 28


// 14. Divide a variable by 2 using the /= operator
let d = 20;
d /= 2; // same as d = d / 2
console.log("Value of d:", d); // 10


// 15. Use the assignment operator to combine a string with another string
let message = "Hello";
message += " World!"; // same as message = message + " World!"
console.log(message); // "Hello World!"
