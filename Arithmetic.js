// 6. Calculate the area of a rectangle (Area = length × width)
let length = 10;
let width = 5;
let area = length * width;
console.log("Area of rectangle:", area); // 50


// 7. Increment a variable by 5 using addition and assignment
let num = 10;
num += 5; // same as num = num + 5
console.log("Incremented value:", num); // 15


// 8. Divide two numbers and find the remainder
let dividend = 20;
let divisor = 6;
let quotient = dividend / divisor;
let remainder = dividend % divisor;
console.log("Quotient:", quotient);   // 3.333...
console.log("Remainder:", remainder); // 2


// 9. Convert Celsius to Fahrenheit (F = (C × 9/5) + 32)
let celsius = 30;
let fahrenheit = (celsius * 9/5) + 32;
console.log(celsius + "°C =", fahrenheit + "°F"); // 30°C = 86°F


// 10. Simple BMI Calculator (BMI = weight / (height²))
let weight = 70; // in kilograms
let height = 1.75; // in meters
let bmi = weight / (height * height);
console.log("BMI:", bmi.toFixed(2)); // 22.86
